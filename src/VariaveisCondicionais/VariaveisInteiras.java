package VariaveisCondicionais;

public class VariaveisInteiras {
    public static void main(String[] args) {
        double valorPassagem = 3.75;
        float valorTomate = 7.50f;
        char letra = 'j';
        System.out.println("O valor da passagem é " + valorPassagem);
        System.out.println("O valor do tomate é " + valorTomate);
        System.out.println("A letra J na tabela ASCII é " + letra);
    }
}
