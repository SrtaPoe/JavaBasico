package DoWhile;

public class QuestaoSeis {
    public static void main(String[] args) {

        //1 a 20 ---> abaixo
        for(int i = 1; i<=20; i++){
            System.out.println(i);
        }
        //1 a 20 --> lado a lado
        for(int i = 1; i<=20; i++)
            System.out.print(i + ", ");

    }
}
